// Apply translations to the page
function applyTranslations(langCode) {
    const translations = getTranslations(langCode);

    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[key];
            } else {
                element.textContent = translations[key];
            }
        }
    });

    // Update page title
    if (translations['page_title']) {
        document.title = translations['page_title'] + ' - PDFrow';
    }
}

// Language switcher integration
function initLanguageSwitcher() {
    const languageItems = document.querySelectorAll('.language-item');
    const languageBtn = document.querySelector('#languageBtn');
    const languageMenu = document.querySelector('#languageMenu');
    const currentLang = document.querySelector('#currentLang');
    const currentFlag = document.querySelector('#currentFlag');

    // Load saved language
    const savedLang = localStorage.getItem('selectedLanguage') || 'en';
    applyTranslations(savedLang);

    // Update language button display
    const savedItem = document.querySelector(`.language-item[data-lang="${savedLang}"]`);
    if (savedItem && currentLang && currentFlag) {
        currentLang.textContent = savedItem.querySelector('span:last-child').textContent;
        currentFlag.textContent = savedItem.getAttribute('data-flag');
    }

    // Toggle menu
    if (languageBtn) {
        languageBtn.addEventListener('click', () => {
            languageMenu.classList.toggle('open');
        });
    }

    // Handle language selection
    languageItems.forEach(item => {
        item.addEventListener('click', () => {
            const lang = item.getAttribute('data-lang');
            const flag = item.getAttribute('data-flag');
            const name = item.querySelector('span:last-child').textContent;

            localStorage.setItem('selectedLanguage', lang);
            applyTranslations(lang);

            if (currentLang) currentLang.textContent = name;
            if (currentFlag) currentFlag.textContent = flag;
            if (languageMenu) languageMenu.classList.remove('open');
        });
    });

    // Close menu when clicking outside
    document.addEventListener('click', (e) => {
        if (languageBtn && languageMenu && !languageBtn.contains(e.target) && !languageMenu.contains(e.target)) {
            languageMenu.classList.remove('open');
        }
    });
}

// Initialize when DOM is ready
document.addEventListener('DOMContentLoaded', function() {
    initLanguageSwitcher();
});
